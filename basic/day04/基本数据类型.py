# 2.基本数据类型
# 数字类型：(int 与float可以相加)
# 整型：int
# 例：age = 18
# 作用：记录年龄、身份证号、数量、个数等等
# 浮点型：float
# 例：salary = 15.5
# 作用：记录薪资、身高、体重、小数等等
# 其他使用：算术运算、比较运算..
# 字符串类型：string
# 作用：记录描述性质的状态、名字、一段话等等
# 定义：用引号（'', "", '''''', """""""）包含的一串字符
# 列表类型：list
# 字典类型：dict
# 定义：{}
# 内用逗号分隔开多个key: value
# 其中value是可变类型、key是不可变类型
# 元祖类型：tuple